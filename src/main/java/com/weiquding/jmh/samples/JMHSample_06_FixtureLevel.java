package com.weiquding.jmh.samples;

import org.openjdk.jmh.annotations.*;
import org.openjdk.jmh.runner.Runner;
import org.openjdk.jmh.runner.RunnerException;
import org.openjdk.jmh.runner.options.Options;
import org.openjdk.jmh.runner.options.OptionsBuilder;

@State(Scope.Thread)
public class JMHSample_06_FixtureLevel {

    double x;

    /*
     * Fixture方法可以有不同的运行控制级别。对于用户可用的级别至少有三个，它们从高到低分别为：
     *
     * Level.Trial: 在整个基准测试周期的开始或结束时（一系列迭代）
     * Level.Iteration: 在一个迭代的开始或结束（一系统方法调用）
     * Level.Invocation; 基准测试方法调用的开始或结束(警告：用之前阅读Javadoc)
     *
     * 在fixture方法上花费的时间不计入性能指标，因此可以用它来完成一些耗时的工作。
     */

    @TearDown(Level.Iteration)
    public void check() {
        assert x > Math.PI : "Nothing changed?";
    }

    @Benchmark
    public void measureRight() {
        x++;
    }

    @Benchmark
    public void measureWrong() {
        double x = 0;
        x++;
    }

    /*
     * ============================== HOW TO RUN THIS TEST: ====================================
     *
     * 你能看到measureRight()方法正确地产生地产生了结果，而measureWrong()在第一个迭代触发了断言。
     * 它将不对measureWrong()方法生成结果。你可以通过设置“fail on error”来阻止JMH继续运行。
     *
     * You can run this test:
     *
     * a) Via the command line:
     *    $ mvn clean install
     *    $ java -ea -jar target/benchmarks.jar JMHSample_06 -f 1
     *    (we requested single fork; there are also other options, see -h)
     *
     *    你可以选择设置-foe选项来提供"fail on error"特性。
     *
     * b) Via the Java API:
     *    (see the JMH homepage for possible caveats when running from IDE:
     *      http://openjdk.java.net/projects/code-tools/jmh/)
     */

    public static void main(String[] args) throws RunnerException {
        Options opt = new OptionsBuilder()
                .include(JMHSample_06_FixtureLevel.class.getSimpleName())
                .forks(1)
                .jvmArgs("-ea")
                .shouldFailOnError(false) // switch to "true" to fail the complete run
                .build();

        new Runner(opt).run();
    }

}
